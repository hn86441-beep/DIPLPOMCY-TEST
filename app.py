WIKI_LANG_MAP = {"ar": "ar", "en": "en", "ru": "ru"}
# ويكيبيديا (وكذلك معظم الـ APIs العامة) تشترط وجود User-Agent وصفي، وإلا ترفض الطلب (403/406).
